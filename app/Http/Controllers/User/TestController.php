            ]);

            $test = new Test();
            $test->test_session_id = $request->test_session_id;
            $test->save();

            return redirect()->route('responses.index',['test_id'=>$test->id])->with('success', 'Successfull started a test');
        }catch(\Exception $e)
        {
            return redirect()->back()->with('error', $e->getMessage());
